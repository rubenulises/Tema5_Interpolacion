public class Punto2 {

    private int i2;
    private double x2;
    private double y2;

    public int getI2() {
        return i2;
    }

    public void setI2(int i2) {
        this.i2 = i2;
    }

    public double getX2() {
        return x2;
    }

    public void setX2(double x2) {
        this.x2 = x2;
    }

    public double getY2() {
        return y2;
    }

    public void setY2(double y2) {
        this.y2 = y2;
    }

    @Override
    public String toString() {
        return "Punto2{" + "i=" + i2 + ", x2=" + x2 + ", y2=" + y2 + '}';
    }

}
